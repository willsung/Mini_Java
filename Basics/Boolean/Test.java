class Test{
	public static void main(String agre[]){
		boolean a = true;
		boolean b = false;
		//boolean c = 0;   布尔型数据只能有true和false两种；
		System.out.println(a);
		System.out.println(b);
		
		
		//要注意：字符型赋值要用单引号！使用双引号表示的是字符串。
		char d = 'm';
		char e = '一';	//字符型数据可以用汉字赋值
		System.out.println(d);
		System.out.println(e);
		}
}